const optionalPlugin = (name) => {
  try {
    return require(name);
  } catch (error) {
    if (process.env.NODE_ENV !== 'production') {
      console.warn(`Optional Tailwind plugin "${name}" skipped: ${error.message}`);
    }
    return null;
  }
};

